<h1 class="text-3xl font-bold text-[#222222] mb-2">Uvjeti korištenja</h1>
<p class="text-sm text-[#45515e] mb-10">Zadnje ažuriranje: 27. travnja 2026.</p>

<div class="space-y-8 text-[#45515e] leading-relaxed">

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">1. Uvod</h2>
        <p>Ovi Uvjeti korištenja ("Uvjeti") reguliraju vaše korištenje platforme LiftDeck ("Usluga"), softverskog rješenja za fitness trenere, kojom upravlja LiftDeck d.o.o., tvrtka registrirana u Republici Sloveniji ("Tvrtka", "mi", "nas" ili "naše").</p>
        <p class="mt-3">Registracijom ili korištenjem Usluge pristajete na ove Uvjete. Ako se ne slažete s Uvjetima, ne smijete koristiti Uslugu.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">2. Definicije</h2>
        <ul class="list-disc pl-5 space-y-2">
            <li><strong class="text-[#222222]">Trener</strong> — registrirani korisnik koji se pretplaćuje na Uslugu radi upravljanja klijentima, programima i planovima prehrane.</li>
            <li><strong class="text-[#222222]">Klijent</strong> — krajnji korisnik koji pristupa Usluzi putem pozivnice Trenera.</li>
            <li><strong class="text-[#222222]">Račun</strong> — registrirani profil stvoren pri prijavi za Uslugu.</li>
            <li><strong class="text-[#222222]">Pretplatnički plan</strong> — plaćeni plan koji odabire Trener, a koji određuje dostupnost funkcija i ograničenje broja klijenata.</li>
        </ul>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">3. Uvjeti korištenja</h2>
        <p>Za korištenje Usluge morate imati najmanje 18 godina. Korištenjem Usluge izjavljujete i jamčite da ispunjavate ovaj uvjet te da su svi podaci koje navedete točni i potpuni.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">4. Registracija računa</h2>
        <p>Odgovorni ste za povjerljivost svojih podataka za prijavu i za sve aktivnosti koje se odvijaju putem vašeg računa. Slažete se da ćete nas o svakom neovlaštenom pristupu vašem računu odmah obavijestiti na <a href="mailto:info@liftdeck.io" class="text-[#1456f0] hover:underline">info@liftdeck.io</a>.</p>
        <p class="mt-3">Zadržavamo pravo na ukidanje računa koji krše ove Uvjete ili koji su bili neaktivni više od 12 mjeseci.</p>
    </section>

