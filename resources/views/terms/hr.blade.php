    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">6. Besplatno probno razdoblje</h2>
        <p>Novi Treneri imaju pravo na 7-dnevno besplatno probno razdoblje. Nakon isteka probnog razdoblja, za nastavak pristupa potrebno je odabrati plaćeni pretplatnički plan. Zadržavamo pravo da ovu ponudu u bilo kojem trenutku izmijenimo ili ukinemo.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">7. Promotivne ponude</h2>
        <p>S vremena na vrijeme nudimo promotivne kodove za popuste. Takvi kodovi podliježu posebnim uvjetima koji se priopćuju u trenutku ponude. Na primjer, promotivni kod <strong class="text-[#222222]">FOUNDING70</strong> daje 70 % popusta za prva dva mjeseca bilo kojeg plaćenog plana i dostupan je isključivo osnivačkim pretplatnicima. Promotivni kodovi ne mogu se kombinirati, prenositi niti primjenjivati retroaktivno.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">8. Plaćanje i naplata</h2>
        <p>Pretplate se naplaćuju mjesečno unaprijed. Plaćanje se sigurno obrađuje putem našeg pružatelja usluga plaćanja. Navođenjem podataka o plaćanju ovlašćujete nas za periodičnu naplatu odgovarajućih naknada sve dok ne otkažete pretplatu.</p>
        <p class="mt-3">U slučaju neuspješnog plaćanja zadržavamo pravo na suspenziju pristupa Usluzi sve dok plaćanje ne bude uspješno obrađeno. Odgovorni ste za točnost i ažurnost vaših podataka o plaćanju.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">9. Otkazivanje pretplate i povrati</h2>
        <p>Pretplatu možete otkazati u bilo kojem trenutku putem postavki vašeg računa. Otkazivanje stupa na snagu na kraju tekućeg obračunskog razdoblja; do tada zadržavate pristup Usluzi.</p>
        <p class="mt-3">Ne nudimo povrate za djelomična obračunska razdoblja ni za neiskorištene dijelove pretplate, osim ako to zahtijeva primjenjivo zakonodavstvo.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">10. Prihvatljivo korištenje</h2>
        <p>Slažete se da ćete Uslugu koristiti samo u zakonite svrhe i u skladu s ovim Uvjetima. Zabranjeno je:</p>
        <ul class="list-disc pl-5 space-y-2 mt-3">
            <li>Putem Usluge slati nezakonite, štetne ili uvredljive sadržaje.</li>
            <li>Pokušavati steći neovlašteni pristup bilo kojem dijelu Usluge ili njezine infrastrukture.</li>
            <li>Rastavljati, dekompilirati ili na drugi način pokušavati dobiti izvorni kod Usluge.</li>
            <li>Preprodavati ili podlicencirati pristup Usluzi trećim stranama.</li>
            <li>Bez našeg pisanog dopuštenja koristiti automatizirana alate za prikupljanje podataka iz Usluge.</li>
        </ul>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">11. Intelektualno vlasništvo</h2>
        <p>Sva prava, naslov i interes u vezi s Uslugom — uključujući softver, dizajn, trgovačke znakove i sadržaj — ostaju isključivo vlasništvo Tvrtke. Ovi Uvjeti ne dodjeljuju vam nikakva vlasnička prava nad Uslugom.</p>
        <p class="mt-3">Vlasništvo nad sadržajem koji učitate ili stvorite unutar Usluge (kao što su programi treninga i podaci klijenata) ostaje vaše. Korištenjem Usluge dodjeljujete nam ograničenu, neisključivu licencu za obradu i pohranjevanje tog sadržaja isključivo u svrhu pružanja Usluge.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">12. Privatnost i zaštita podataka</h2>
        <p>Prikupljanje i korištenje osobnih podataka uređeno je našom Politikom privatnosti. Korištenjem Usluge potvrđujete da ste pročitali i razumjeli Politiku privatnosti.</p>
        <p class="mt-3">Kao Trener koji putem Usluge obrađuje osobne podatke vaših Klijenata, nastupate kao voditelj obrade tih podataka. Odgovorni ste za pribavljanje svih potrebnih privola i za poštovanje primjenjivog zakonodavstva o zaštiti podataka, uključujući Uredbu GDPR.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">13. Dostupnost usluge</h2>
        <p>Nastojimo osigurati visoku dostupnost Usluge, ali ne jamčimo neprekinut pristup. Možemo provoditi održavanje koje privremeno utječe na dostupnost, a nastojat ćemo o tome unaprijed obavijestiti gdje je to moguće.</p>
        <p class="mt-3">Nismo odgovorni za bilo kakvu štetu nastalu zbog privremene nedostupnosti Usluge.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">14. Odricanje od jamstava</h2>
        <p>Usluga se pruža "kakva jest" i "kako je dostupna", bez ikakvih izričitih ili implicitnih jamstava, uključujući, ali ne ograničavajući se na, jamstva o tržišnoj podobnosti, prikladnosti za određenu svrhu ili nekršenju prava. Ne jamčimo da će Usluga biti bez grešaka niti da će nedostaci biti ispravljeni.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">15. Ograničenje odgovornosti</h2>
        <p>U mjeri dopuštenoj primjenjivim zakonodavstvom, Tvrtka neće biti odgovorna za bilo kakvu neizravnu, slučajnu, posebnu, posljedičnu ili kaznenu štetu, uključujući gubitak dobiti, podataka ili ugleda, nastalu u vezi s vašim korištenjem Usluge.</p>
        <p class="mt-3">Naša ukupna odgovornost prema vama za sve zahtjeve prema ovim Uvjetima neće prelaziti ukupni iznos koji ste nam platili u tri mjeseca koja prethode događaju koji je doveo do zahtjeva.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">16. Izmjene Usluge i Uvjeta</h2>
        <p>Zadržavamo pravo da u bilo kojem trenutku izmijenimo ili ukinemo Uslugu (ili njezin dio). O bitnim izmjenama ovih Uvjeta obavijestit ćemo vas e-poštom ili obavijesti unutar Usluge. Nastavak korištenja Usluge nakon stupanja izmjena na snagu smatra se prihvaćanjem izmijenjenih Uvjeta.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">17. Mjerodavno pravo i rješavanje sporova</h2>
        <p>Ovi Uvjeti uređuju se i tumače u skladu sa zakonodavstvom Republike Slovenije, bez primjene kolizijskih pravila. Svaki spor koji proizlazi iz ovih Uvjeta podliježe isključivoj nadležnosti nadležnih sudova Republike Slovenije.</p>
    </section>

