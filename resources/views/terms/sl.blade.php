    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">6. Brezplačno preizkusno obdobje</h2>
        <p>Novi Trenerji so upravičeni do 7-dnevnega brezplačnega preizkusnega obdobja. Po izteku preizkusnega obdobja je za nadaljnji dostop potrebna izbira plačljivega naročniškega paketa. Pridržujemo si pravico, da to ponudbo kadar koli spremenimo ali ukinemo.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">7. Promocijske ponudbe</h2>
        <p>Občasno ponujamo promocijske kode za popuste. Te kode so predmet posebnih pogojev, ki so navedeni ob ponudbi. Na primer, promocijska koda <strong class="text-[#222222]">FOUNDING70</strong> zagotavlja 70 % popust za prva dva meseca katerega koli plačljivega paketa in je na voljo izključno ustanovnim naročnikom. Promocijskih kod ni mogoče kombinirati, prenesti ali uveljavljati za nazaj.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">8. Plačilo in obračun</h2>
        <p>Naročnine se zaračunavajo mesečno vnaprej. Plačilo se varno obdela prek našega ponudnika plačilnih storitev. Z navedbo plačilnih podatkov nas pooblastite za periodično zaračunavanje ustreznih nadomestil, dokler ne prekinete naročnine.</p>
        <p class="mt-3">V primeru neuspešnega plačila si pridržujemo pravico, da prekinemo dostop do Storitve, dokler plačilo ni uspešno obdelano. Odgovorni ste za zagotavljanje točnosti in aktualnosti vaših plačilnih podatkov.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">9. Prekinitev naročnine in povračila</h2>
        <p>Naročnino lahko kadar koli prekinete prek nastavitev svojega računa. Prekinitev začne veljati ob koncu tekočega obračunskega obdobja; do takrat ohranite dostop do Storitve.</p>
        <p class="mt-3">Povračil za delna obračunska obdobja ali neporabljene dele naročnine ne nudimo, razen kjer to zahteva veljavna zakonodaja.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">10. Sprejemljiva uporaba</h2>
        <p>Strinjate se, da boste Storitev uporabljali le za zakonite namene in v skladu s temi Pogoji. Prepovedano je:</p>
        <ul class="list-disc pl-5 space-y-2 mt-3">
            <li>Prek Storitve pošiljati nezakonite, škodljive ali žaljive vsebine.</li>
            <li>Poskušati pridobiti nepooblaščen dostop do katerega koli dela Storitve ali njene infrastrukture.</li>
            <li>Razstavljati, dekompilirati ali kako drugače poskušati pridobiti izvorno kodo Storitve.</li>
            <li>Preprodajati ali podlicencirati dostop do Storitve tretjim osebam.</li>
            <li>Brez našega pisnega dovoljenja uporabljati avtomatizirane orodja za pridobivanje podatkov iz Storitve.</li>
        </ul>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">11. Intelektualna lastnina</h2>
        <p>Vse pravice, naslov in interes v zvezi s Storitvijo — vključno s programsko opremo, oblikovanjem, blagovnimi znamkami in vsebino — ostanejo izključna last Podjetja. Ti Pogoji vam ne podeljujejo nobenih lastniških pravic nad Storitvijo.</p>
        <p class="mt-3">Lastništvo nad vsebino, ki jo naložite ali ustvarite v Storitvi (kot so vadbeni programi in podatki strank), ostane pri vas. Z uporabo Storitve nam podeljujete omejeno, neizključno licenco za obdelavo in shranjevanje te vsebine izključno z namenom zagotavljanja Storitve.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">12. Zasebnost in varstvo podatkov</h2>
        <p>Zbiranje in uporaba osebnih podatkov je urejena z našo Politiko zasebnosti. Z uporabo Storitve potrjujete, da ste prebrali in razumeli Politiko zasebnosti.</p>
        <p class="mt-3">Kot Trener, ki prek Storitve obdeluje osebne podatke vaših Strank, nastopate kot upravljavec teh podatkov. Odgovorni ste za pridobitev vseh potrebnih privolitev in za spoštovanje veljavne zakonodaje o varstvu podatkov, vključno z Uredbo GDPR.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">13. Razpoložljivost Storitve</h2>
        <p>Prizadevamo si za visoko razpoložljivost Storitve, vendar ne zagotavljamo neprekinjenega dostopa. Morda bomo izvajali vzdrževalna dela, ki začasno vplivajo na razpoložljivost, pri čemer bomo skušali zagotoviti predhodno obvestilo, kjer je to izvedljivo.</p>
        <p class="mt-3">Za morebitno izgubo ali škodo, ki bi nastala zaradi začasne nedostopnosti Storitve, nismo odgovorni.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">14. Izjava o omejitvi garancij</h2>
        <p>Storitev je zagotovljena "takšna, kakršna je" in "kot je na voljo", brez kakršnih koli izrecnih ali implicitnih garancij, vključno z, a ne omejeno na, garancijami o tržnosti, primernosti za določen namen ali nekršitvi pravic. Ne jamčimo, da bo Storitev brez napak ali da bodo pomanjkljivosti odpravljene.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">15. Omejitev odgovornosti</h2>
        <p>V obsegu, ki ga dopušča veljavna zakonodaja, Podjetje ni odgovorno za posredno, naključno, posebno, posledično ali kaznujočo škodo, vključno z izgubo dobička, podatkov ali ugleda, ki bi nastala v zvezi z vašo uporabo Storitve.</p>
        <p class="mt-3">Naša skupna odgovornost do vas za kakršne koli zahtevke po teh Pogojih ne presega skupnega zneska, ki ste ga plačali v treh mesecih pred nastankom škode.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">16. Spremembe Storitve in Pogojev</h2>
        <p>Pridržujemo si pravico, da kadar koli spremenimo ali ukinemo Storitev (ali njen del). O bistvenih spremembah teh Pogojev vas bomo obvestili po e-pošti ali z obvestilom v Storitvi. Nadaljnja uporaba Storitve po uveljavitvi sprememb pomeni sprejem posodobljenih Pogojev.</p>
    </section>

