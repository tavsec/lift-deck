<div class="space-y-8 text-[#45515e] leading-relaxed">

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">1. Uvod</h2>
        <p>Ti splošni pogoji uporabe ("Pogoji") urejajo vašo uporabo platforme LiftDeck ("Storitev"), programske rešitve za fitnes trenerje, ki jo upravlja LiftDeck d.o.o., podjetje registrirano v Republiki Sloveniji ("Podjetje", "mi", "nas" ali "naše").</p>
        <p class="mt-3">Z registracijo ali uporabo Storitve soglašate s temi Pogoji. Če se s Pogoji ne strinjate, Storitve ne smete uporabljati.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">2. Opredelitev pojmov</h2>
        <ul class="list-disc pl-5 space-y-2">
            <li><strong class="text-[#222222]">Trener</strong> — registriran uporabnik, ki se naroči na Storitev za upravljanje strank, programov in prehranskih načrtov.</li>
            <li><strong class="text-[#222222]">Stranka</strong> — končni uporabnik, ki dostopa do Storitve na podlagi povabila Trenerja.</li>
            <li><strong class="text-[#222222]">Račun</strong> — registriran profil, ustvarjen ob prijavi v Storitev.</li>
            <li><strong class="text-[#222222]">Naročniški paket</strong> — plačljivi paket, ki ga izbere Trener in določa dostop do funkcij ter omejitev števila strank.</li>
        </ul>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">3. Pogoji za uporabo</h2>
        <p>Za uporabo Storitve morate biti stari vsaj 18 let. Z uporabo Storitve zagotavljate in jamčite, da izpolnjujete to zahtevo ter da so vsi podatki, ki jih posredujete, točni in popolni.</p>
    </section>

    <section>
        <h2 class="text-xl font-semibold text-[#222222] mb-3">4. Registracija računa</h2>
        <p>Odgovorni ste za zaupnost svojih prijavnih podatkov in za vse dejavnosti, ki potekajo pod vašim računom. Strinjate se, da nas boste o vsakem nepooblaščenem dostopu do vašega računa nemudoma obvestili na <a href="mailto:info@liftdeck.io" class="text-[#1456f0] hover:underline">info@liftdeck.io</a>.</p>
        <p class="mt-3">Pridržujemo si pravico do prekinitve računov, ki kršijo te Pogoje ali ki so bili neaktivni več kot 12 mesecev.</p>
    </section>

